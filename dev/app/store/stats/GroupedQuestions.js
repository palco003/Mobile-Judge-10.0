    extend: 'Ext.data.Store',
    alias: 'store.questionAverage',
    model: 'MobileJudge.model.stats.QuestionAverage',
    data: (function () {
        var data = [];
        var expense = Ext.createByAlias('store.questionGrades');
        console.log(expense);
        expense.group('questionId');
        var groups = expense.getGroups();
        console.log(groups);
        groups.each(function (group) {
            console.log(group);
            data.push({ questionId: group.config.groupKey, average: group.average('grade') });
        });
        return data;
    })()
